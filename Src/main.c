/* USER CODE BEGIN 0 */
uint8_t absolute(int8_t input)
{
	return (~input) + 1;
}

/* Interrupt function ----------------------------------------------------*/

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	uint8_t status;
	uint8_t rxbyte = status_register(RXBYTES, RW_READING, &status);
	
	while( rxbyte / (CC1101_PKT_SIZE + 2) )
	{
		status = fifo_access((uint8_t *)&(write->payload), CC1101_PKT_SIZE + 2, RW_READING);
		
		write->status = BUSY;
		write = write->next;
		
		rxbyte = status_register(RXBYTES, RW_READING, &status);
	}
}

